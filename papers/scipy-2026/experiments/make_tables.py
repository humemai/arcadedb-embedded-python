#!/usr/bin/env python3
"""Generate the paper's result tables from results/runs.csv.

Aggregates the 5 reps per (lane, backend, dataset, workload) cell into mean (+/- stdev)
and emits Markdown tables to results/tables.md. Results go in tables, not figures
(see ../README and the paper PLAN). Run after run.py:

    python make_tables.py
"""
import csv
import collections
import statistics as st

HERE = __file__.rsplit("/", 1)[0]
RUNS = f"{HERE}/results/runs.csv"
OUT = f"{HERE}/results/tables.md"

TIERS = ["tiny", "small", "medium"]
TIER_LABEL = {"tiny": "tiny", "small": "small (CS)", "medium": "medium (CV)"}


def fnum(x):
    try:
        return float(x)
    except (TypeError, ValueError):
        return None


def load():
    g = collections.defaultdict(list)
    for r in csv.DictReader(open(RUNS)):
        g[(r["lane"], r["backend"], r["dataset"], r.get("workload", ""))].append(r)
    return g


def agg(g, cell, key):
    vals = [fnum(r.get(key, "")) for r in g[cell]]
    vals = [v for v in vals if v is not None]
    if not vals:
        return None
    return st.mean(vals), (st.pstdev(vals) if len(vals) > 1 else 0.0)


def cell_str(g, cell, key, fmt="{:.0f}", scale=1.0):
    a = agg(g, cell, key)
    if a is None:
        return "--"
    m, s = a[0] * scale, a[1] * scale
    return f"{fmt.format(m)} ± {fmt.format(s)}"


def mean_only(g, cell, key, fmt="{:.0f}", scale=1.0):
    a = agg(g, cell, key)
    return "--" if a is None else fmt.format(a[0] * scale)


def main():
    g = load()
    out = []
    w = out.append

    w("# Result tables (generated by make_tables.py)\n")
    w("Mean ± stdev over 5 reps. cpuset 0-7 (8 cores), per-tier memory cap.")
    w("Tiers: tiny / small = cs.stackexchange / medium = stats.stackexchange (Cross Validated).\n")

    # ---- T2a: tabular ----
    w("## Tabular (documents/relational): SQLite, DuckDB, ArcadeDB\n")
    w("OLTP = mixed point read/insert/update workload (ops/s, higher better). "
      "OLAP = analytical aggregation suite (total ms, lower better). Load = bulk ingest (s).\n")
    w("| Tier | Backend | OLTP ops/s | OLAP ms | Ingest s | Peak MiB | DB MiB |")
    w("|------|---------|-----------:|--------:|---------:|---------:|-------:|")
    for ds in TIERS:
        for be in ["sqlite", "duckdb", "arcadedb"]:
            ol = ("tabular", be, ds, "oltp")
            ap = ("tabular", be, ds, "olap")
            w(f"| {TIER_LABEL[ds]} | {be} | {cell_str(g, ol, 'oltp_ops_per_s')} "
              f"| {cell_str(g, ap, 'olap_total_ms', '{:.1f}')} "
              f"| {cell_str(g, ol, 'ingest_s', '{:.2f}')} "
              f"| {cell_str(g, ol, 'peak_mib', '{:.0f}')} "
              f"| {mean_only(g, ol, 'db_size_mb', '{:.1f}')} |")
    w("")

    # ---- T2b: graph ----
    w("## Graph (property graph): LadybugDB, ArcadeDB\n")
    w("OLTP = neighborhood/traversal point ops (ops/s). OLAP = multi-query analytical suite "
      "(total ms). ArcadeDB OLAP uses a Graph Analytical View (one-time build, s).\n")
    w("| Tier | Backend | OLTP ops/s | OLAP ms | GAV build s | Ingest s | Peak MiB | DB MiB |")
    w("|------|---------|-----------:|--------:|------------:|---------:|---------:|-------:|")
    for ds in TIERS:
        for be in ["ladybug", "arcadedb"]:
            ol = ("graph", be, ds, "oltp")
            ap = ("graph", be, ds, "olap")
            gav = cell_str(g, ap, "gav_build_s", "{:.2f}")
            w(f"| {TIER_LABEL[ds]} | {be} | {cell_str(g, ol, 'oltp_ops_per_s')} "
              f"| {cell_str(g, ap, 'olap_total_ms', '{:.1f}')} | {gav} "
              f"| {cell_str(g, ol, 'ingest_s', '{:.2f}')} "
              f"| {cell_str(g, ol, 'peak_mib', '{:.0f}')} "
              f"| {mean_only(g, ol, 'db_size_mb', '{:.1f}')} |")
    w("")

    # ---- T2c: vector ----
    w("## Vector (ANN, HNSW @ M=16, ef_construction=100, ef_search=100): Chroma, ArcadeDB\n")
    w("Matched HNSW parameters. Build = insert+index (s). Query = mean latency (ms). "
      "recall@10 vs exact ground truth.\n")
    w("| Tier | Backend | # vectors | Build s | Query ms | recall@10 | Peak MiB | DB MiB |")
    w("|------|---------|----------:|--------:|---------:|----------:|---------:|-------:|")
    for ds in TIERS:
        for be in ["chroma", "arcadedb"]:
            c = ("vector", be, ds, "")
            w(f"| {TIER_LABEL[ds]} | {be} | {mean_only(g, c, 'n_vectors', '{:.0f}')} "
              f"| {cell_str(g, c, 'build_s', '{:.1f}')} "
              f"| {cell_str(g, c, 'q_mean_ms', '{:.2f}')} "
              f"| {cell_str(g, c, 'recall@10', '{:.3f}')} "
              f"| {cell_str(g, c, 'peak_mib', '{:.0f}')} "
              f"| {mean_only(g, c, 'db_size_mb', '{:.1f}')} |")
    w("")

    # ---- T3: lifecycle phase timings (medium tier) ----
    w("## Lifecycle phase timings (medium tier, seconds)\n")
    w("Per-phase wall time isolated by the harness: module import, JVM init (ArcadeDB only), "
      "DB open, schema/DDL, bulk ingest, index build (HNSW / GAV / secondary), and close.\n")
    w("| Lane | Backend | import | jvm_init | open | schema | ingest | index_build | close |")
    w("|------|---------|-------:|---------:|-----:|-------:|-------:|------------:|------:|")
    lifecycle_cells = [
        ("vector", "chroma", ""), ("vector", "arcadedb", ""),
        ("tabular", "sqlite", "olap"), ("tabular", "duckdb", "olap"), ("tabular", "arcadedb", "olap"),
        ("graph", "ladybug", "olap"), ("graph", "arcadedb", "olap"),
    ]
    for lane, be, wl in lifecycle_cells:
        c = (lane, be, "medium", wl)
        w(f"| {lane} | {be} "
          f"| {mean_only(g, c, 'import_s', '{:.3f}')} "
          f"| {mean_only(g, c, 'jvm_init_s', '{:.3f}')} "
          f"| {mean_only(g, c, 'open_s', '{:.3f}')} "
          f"| {mean_only(g, c, 'schema_s', '{:.3f}')} "
          f"| {mean_only(g, c, 'ingest_s', '{:.2f}')} "
          f"| {mean_only(g, c, 'index_build_s', '{:.2f}')} "
          f"| {mean_only(g, c, 'close_s', '{:.3f}')} |")
    w("")

    # ---- T4: query latency distribution (medium tier, ms) ----
    w("## Query latency distribution (medium tier, ms)\n")
    w("Per-operation latency percentiles over all timed ops (5 reps pooled per cell shown as "
      "the mean of per-rep summaries). p99 captures tail behavior.\n")
    w("| Lane / op | Backend | p50 | p99 | max |")
    w("|-----------|---------|----:|----:|----:|")
    lat_rows = [
        ("vector query", "vector", "chroma", "", "q"),
        ("vector query", "vector", "arcadedb", "", "q"),
        ("tabular read", "tabular", "sqlite", "oltp", "read"),
        ("tabular read", "tabular", "duckdb", "oltp", "read"),
        ("tabular read", "tabular", "arcadedb", "oltp", "read"),
        ("graph point", "graph", "ladybug", "oltp", "point"),
        ("graph point", "graph", "arcadedb", "oltp", "point"),
        ("graph hop", "graph", "ladybug", "oltp", "hop"),
        ("graph hop", "graph", "arcadedb", "oltp", "hop"),
    ]
    for label, lane, be, wl, op in lat_rows:
        c = (lane, be, "medium", wl)
        w(f"| {label} | {be} "
          f"| {mean_only(g, c, f'{op}_p50_ms', '{:.3f}')} "
          f"| {mean_only(g, c, f'{op}_p99_ms', '{:.3f}')} "
          f"| {mean_only(g, c, f'{op}_max_ms', '{:.2f}')} |")
    w("")

    open(OUT, "w").write("\n".join(out) + "\n")
    print(f"wrote {OUT}")
    print("\n".join(out))


if __name__ == "__main__":
    main()
